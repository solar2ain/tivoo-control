def gradient():
    """RGB gradient test pattern."""
    pixels = []
    for y in range(16):
        row = []
        for x in range(16):
            r = int(255 * x / 15)
            g = int(255 * y / 15)
            b = int(255 * (1 - x / 15))
            row.append((r, g, b))
        pixels.append(row)
    return pixels


def rainbow():
    """Rainbow stripes."""
    colors = [
        (255, 0, 0), (255, 0, 0),
        (255, 127, 0), (255, 127, 0),
        (255, 255, 0), (255, 255, 0),
        (0, 200, 0), (0, 200, 0),
        (0, 150, 255), (0, 150, 255),
        (75, 0, 130), (75, 0, 130),
        (148, 0, 211), (148, 0, 211),
        (255, 0, 0), (255, 0, 0),
    ]
    return [[colors[y]] * 16 for y in range(16)]


